    private void jcExamIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcExamIDActionPerformed
       userid=txtUserid.getText();
       examid=jcExamID.getSelectedItem().toString();
       if(jcExamID.getSelectedIndex()==0)
       {
           JOptionPane.showMessageDialog(null, "Please choose from the options given below!!", "ALERT!!", JOptionPane.INFORMATION_MESSAGE);
           return;
       }
       try
       {
          StudentScore obj=PerformanceDAO.getScore(userid,examid);
            txtLanguage.setText(obj.getLanguage());
            txtPerc.setText(String.valueOf(obj.getPer()));  
       }
       catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error", "ViewResultFrame Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcExamIDActionPerformed

