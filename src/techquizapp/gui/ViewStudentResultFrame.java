    private void loadExamId() {
        userid=txtUserid.getText();
        try
        {
            ArrayList<String> examIdList=PerformanceDAO.getAllExamid(userid);
            for(String exam:examIdList)
                jcExamID.addItem(exam);
        }
         catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error", "ViewResultFrame Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
