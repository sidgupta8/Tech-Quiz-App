    private boolean validateInputs()
    {
      username=txtUsername.getText();
      password=txtPassword.getText();
      newpwd=txtNewPwd.getText();
      if(username.isEmpty()|| password.isEmpty() || newpwd.isEmpty())
         return false;
      return true;
    }
