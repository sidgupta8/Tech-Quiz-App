    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        boolean isValidInput=validateInputs();
        if(isValidInput==false)
        {
            JOptionPane.showMessageDialog(null, "Username and Password can't be left blank!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!password.equals(newpwd)){
           try{
            Student stud=new Student();
            stud.setPassword(newpwd);
            stud.setUserid(username);
            boolean response=UserDAO.ChangePwd(stud);
            if(response)
            {
                JOptionPane.showMessageDialog(null, "Updation Completed Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                LoginFrame loginFrame=new LoginFrame();
                loginFrame.setVisible(true);
                this.dispose();
            }
            else
            {
                 JOptionPane.showMessageDialog(null,"User dose not exist","Updation failed!",JOptionPane.ERROR_MESSAGE);
            }
        } 
           catch(SQLException ex)
           {
              JOptionPane.showMessageDialog(null, "DB Error", "Change Password Error", JOptionPane.ERROR_MESSAGE);
              ex.printStackTrace();
           }
    }
        else
        {
             JOptionPane.showMessageDialog(null,"Password and NewPassword should be different!!","Updation failed!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

