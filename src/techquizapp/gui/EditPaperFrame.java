    private void btnEditQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuestionActionPerformed
        boolean result=validateInput();
        if(!result)
        {
           JOptionPane.showMessageDialog(null, "Please select Subject!", "Error!", JOptionPane.ERROR_MESSAGE);
           return;
        }
        String subject=jcLanguage.getSelectedItem().toString();
        String examid=jcExamid.getSelectedItem().toString();
        int ans;
        ans=JOptionPane.showConfirmDialog(null, "Are you sure you want to edit examid "+examid+" of subject "+subject+" ?", "Sure?", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
       
        if(ans==JOptionPane.YES_OPTION)
        {
          try
          {
              int totalQues=ExamDAO.getQuestionCountByExam(examid);
              int time=ExamDAO.getTimeOfExam(examid);
              //System.out.println("ExamId "+examid+"\nTotal Question "+totalQues);
              Exam editExam=new Exam(examid,subject,totalQues,time);
              EditQuestionsFrame questionsFrame=new EditQuestionsFrame(editExam);
              questionsFrame.setVisible(true);
              this.dispose();
          }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error", "Edit Paper Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }      
        
      }    
    }//GEN-LAST:event_btnEditQuestionActionPerformed

