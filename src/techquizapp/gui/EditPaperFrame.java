    private boolean validateInput()
    {
        int selectedIndex=jcLanguage.getSelectedIndex();
        if(selectedIndex==0)
            return false;
        subjectName=jcLanguage.getSelectedItem().toString();
        return true;
    }
    private void jcLanguageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcLanguageActionPerformed
       boolean result=validateInput();
       if(!result)
       {
           JOptionPane.showMessageDialog(null, "Please select Subject!", "Error!", JOptionPane.ERROR_MESSAGE);
           jcExamid.removeAllItems();
           return;
       }
       // System.out.println("Subject Name:"+subjectName);
       try
       {
           ArrayList<String> examList=ExamDAO.getExamIdBySubject(subjectName);
           if(examList.isEmpty())
           {
              JOptionPane.showMessageDialog(null, "No exam set for "+subjectName+" !", "Error!", JOptionPane.ERROR_MESSAGE);
              jcLanguage.setSelectedIndex(0);
              jcExamid.removeAllItems();
              return; 
           }
           jcExamid.removeAllItems();
           for(String examId:examList)
           {
               jcExamid.addItem(examId);
           }
       }
       catch(SQLException ex)
       {
            JOptionPane.showMessageDialog(null, "DB Error", "Edit Paper Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
    }//GEN-LAST:event_jcLanguageActionPerformed

